// isEven.js

function isEven(n)
{
    let result = n % 2 === 0;

    return result;
}

//----//

console.log(isEven(5));

//----//

/*
false
*/

//----//

// checks if a number is even

//----//

// % 2 gives the remainder after dividing by 2

// even numbers leave a remainder of 0, so n % 2 === 0 is true only for even numbers

