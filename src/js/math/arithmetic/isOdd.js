// isOdd.js

// checks if a number n is odd
function isOdd(n)
{
    // % 2 gives the remainder after dividing by 2
    // odd numbers leave a remainder of 1
    // so n % 2 !== 0 is true only for odd numbers
    let result = n % 2 !== 0;

    // return the boolean result
    return result;
}

//----//

// example usage: check if 5 is odd
console.log(isOdd(5));

//----//

/*
true
*/

