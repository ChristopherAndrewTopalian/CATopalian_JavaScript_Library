// rectanglePerimeter.js

function rectanglePerimeter(a, b)
{
    let perimeter = (2 * a) + (2 * b);

    return perimeter;
}

//----//

console.log(rectanglePerimeter(20.7, 8.25));

//----//

/*
57.9
*/

